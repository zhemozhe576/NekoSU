@Composable
fun NekoTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = true,
    content: @Composable () -> Unit
) {
    val context = LocalContext.current
    val colorScheme: ColorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }
        darkTheme -> NekoDarkColorScheme
        else -> NekoLightColorScheme
    }

    CompositionLocalProvider(LocalNekoColorScheme provides colorScheme) {
        content()
    }
}

val LocalNekoColorScheme = staticCompositionLocalOf<ColorScheme> { NekoLightColorScheme }
